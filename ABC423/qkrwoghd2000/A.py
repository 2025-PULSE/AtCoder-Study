#1000엔단위로 수수료로가 달라지며, C는 수수료, X는 현재 지갑에 들어있는 돈
# 예외 0엔 미만이면 출금 불가
# 출력 요청 출금할 수 있는 최대 금액
# 수수료 포함한 가격이 원 가격을 넘어가지 않게 최대로 출금할 수 있는 상한 선을 구해라
# 수학 문제로 보인다. V가 출금할 금액이라고 한다면 V+(V/1000)*C<=X인 V를 구하는 것이다.
# 1000*(x/(1000+c))로 보인다.
# 이제 구현 해보자.
# 1000단위이니 //까지 사용했다.
# AC
x,c=map(int,input().split())
print(1000*(x//(1000+c)))